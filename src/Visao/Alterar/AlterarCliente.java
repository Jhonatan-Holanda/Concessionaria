    public void InserirDados(int cod){
        Connection con = Conexao.AbrirConexao();
        ClienteDAO sql = new ClienteDAO(con);
        List<Cliente> lista = new ArrayList<>();
        lista = sql.CapturarCliente(cod);
        
        for(Cliente a : lista){
            jTextField1.setText(""+a.getCod());
            jTextField2.setText(""+a.getNome());
            jTextField3.setText(""+a.getEmail());
            jTextField4.setText(""+a.getCidade());
            jTextField5.setText(""+a.getEstado());
            jFormattedTextField1.setText(""+a.getRG());
            jFormattedTextField2.setText(""+a.getCPF());
            jFormattedTextField3.setText(""+a.getTelefone());
            jTextField6.setText(""+a.getCNH());
        }
        Conexao.FecharConexao(con);
    }
    
    public void AtualizarCombo(){
        Connection con = Conexao.AbrirConexao();
        ClienteDAO sql = new ClienteDAO(con);
        List<Cliente> lista = new ArrayList<>();
        lista = sql.ListarComboCliente();
        
        for(Cliente b : lista){
            jComboBox1.addItem(b.getNome());
        }
        Conexao.FecharConexao(con);
    }
    
