    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cod = jTextField1.getText().toUpperCase();
        String nome = jTextField2.getText().toUpperCase();
        String email = jTextField3.getText();
        String cidade = jTextField4.getText().toUpperCase();
        String estado = jTextField5.getText().toUpperCase();
        String rg = jFormattedTextField1.getText();
        String cpf = jFormattedTextField2.getText();
        String telefone = jFormattedTextField3.getText();
        String cnh = jTextField6.getText().toUpperCase();
        

        if(nome.equals("") || email.equals("") || cidade.equals("") || estado.equals("") || rg.equals("") || cpf.equals("") || telefone.equals("") || cnh.equals("")){
            JOptionPane.showMessageDialog(null, "Nenhum campo pode ficar em branco","Video Locadora",JOptionPane.WARNING_MESSAGE);
        }else{
            Connection con = Conexao.AbrirConexao();
            ClienteDAO sql = new ClienteDAO(con);
            int code = Integer.parseInt(cod);
            Cliente a = new Cliente();
           
            a.setCod(code);
            a.setNome(nome);
            a.setEmail(email);
            a.setCidade(cidade);
            a.setEstado(estado);                        
            a.setRG(rg);
            a.setCPF(cpf);
            a.setTelefone(telefone);
            a.setCNH(cnh);

            sql.Alterar_Cliente(a);
            Conexao.FecharConexao(con);

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jFormattedTextField1.setText("");
            jFormattedTextField2.setText("");
            jFormattedTextField3.setText("");
            jTextField6.setText("");

            JOptionPane.showMessageDialog(null, "Alteração Realizada com sucesso","Video Locadora",JOptionPane.WARNING_MESSAGE);
        }
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

