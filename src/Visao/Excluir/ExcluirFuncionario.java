    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = jComboBox1.getSelectedItem().toString();

        Connection con = Conexao.AbrirConexao();
        FuncionarioDAO sql = new FuncionarioDAO(con);
        Funcionario a = new Funcionario();

        if(nome.equals("Selecione um Funcionario")){
            JOptionPane.showMessageDialog(null, "Nenhum Funcionario selecionado","concessionaria",JOptionPane.QUESTION_MESSAGE);
        }else{
            int b = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir"+"\n("+nome+")","concessionaria",JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(b == 0){
                a.setNome(nome);
                sql.Excluir_Funcionario(a);
                Conexao.FecharConexao(con);
                dispose();
            }
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

